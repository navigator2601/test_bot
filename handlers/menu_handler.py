    access_level = await get_user_access_level(db_pool, user_id)
    if access_level is None:
        access_level = 0
    
    # –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞, —á–∏ –º–∞—î –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á —Ä—ñ–≤–µ–Ω—å –¥–æ—Å—Ç—É–ø—É 10 –∞–±–æ –≤–∏—â–µ –¥–ª—è –∞–¥–º—ñ–Ω-–ø–∞–Ω–µ–ª—ñ
