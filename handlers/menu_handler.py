        return "üåê –ì—ñ—Å—Ç—å –°–∏—Å—Ç–µ–º–∏", "–ú–∞—î –º—ñ–Ω—ñ–º–∞–ª—å–Ω–∏–π –¥–æ—Å—Ç—É–ø –¥–æ –±–∞–∑–æ–≤–∏—Ö —Ñ—É–Ω–∫—Ü—ñ–π."
