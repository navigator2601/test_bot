    logger.info(f"–ö–æ—Ä–∏—Å—Ç—É–≤–∞—á {user_name} (ID: {user_id}) –Ω–∞—Ç–∏—Å–Ω—É–≤ –∫–Ω–æ–ø–∫—É '‚öôÔ∏è –ê–¥–º—ñ–Ω—ñ—Å—Ç—Ä—É–≤–∞–Ω–Ω—è'.")

    if not db_pool:
        logger.error("db_pool –Ω–µ –∑–Ω–∞–π–¥–µ–Ω–æ –≤ –∞—Ä–≥—É–º–µ–Ω—Ç–∞—Ö handle_admin_button!")
